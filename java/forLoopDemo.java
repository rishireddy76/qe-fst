
public class forLoopDemo {
	// Java program to illustrate "for" loop. 
	
	    public static void main(String args[]) {
	        // for loop begins when x=0
	        // and runs till x <=10
	        
	        for (int x = 0; x <= 10; x++)
	            System.out.println("Value of x:" + x);
	    }
	}


